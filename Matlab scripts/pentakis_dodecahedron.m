function [az,el] = pentakis_dodecahedron()

% Pentakis Dodecahedron

C0 = 0.927050983124842272306880251548;% = 3 * (sqrt(5) - 1) / 4
C1 = 1.33058699733550141141687582919;%  = 9 * (9 + sqrt(5)) / 76
C2 = 2.15293498667750705708437914596;%  = 9 * (7 + 5 * sqrt(5)) / 76
C3 = 2.427050983124842272306880251548;% = 3 * (1 + sqrt(5)) / 4

V = [    0.0,   C0,   C3;
         0.0,   C0,  -C3;
         0.0,  -C0,   C3;
         0.0,  -C0,  -C3;
          C3,  0.0,   C0;
          C3,  0.0,  -C0;
         -C3,  0.0,   C0;
         -C3,  0.0,  -C0;
          C0,   C3,  0.0;
          C0,  -C3,  0.0;
         -C0,   C3,  0.0;
         -C0,  -C3,  0.0;
          C1,  0.0,   C2;
          C1,  0.0,  -C2;
         -C1,  0.0,   C2;
         -C1,  0.0,  -C2;
          C2,   C1,  0.0;
          C2,  -C1,  0.0;
         -C2,   C1,  0.0;
         -C2,  -C1,  0.0;
         0.0,   C2,   C1;
         0.0,   C2,  -C1;
         0.0,  -C2,   C1;
         0.0,  -C2,  -C1;
         1.5,  1.5,  1.5;
         1.5,  1.5, -1.5;
         1.5, -1.5,  1.5;
         1.5, -1.5, -1.5;
        -1.5,  1.5,  1.5;
        -1.5,  1.5, -1.5;
        -1.5, -1.5,  1.5;
        -1.5, -1.5, -1.5;
    ];

[az,el,~] = cart2sph(V(:,1),V(:,2),V(:,3));
